# Function to display the Title when the game starts.
def display_hangman_title():
    print(HANGMAN_TITLE)

"""
Stage means the stage at which one-by-one the hangman's body gets completed, when it proceeds towards death.
"""

def get_max_stage():
    return len(HANGMANPICS)-1

def display_hangman_stage(stage: int):
    """
    stage is 0 indexed, i.e, it starts from 0
    """
    stage = abs(stage)
    stage = stage if stage <= get_max_stage() else get_max_stage()
    print( HANGMANPICS[stage] )


